/* ------------------------
    STRING TO lower CASE
--------------------------*/
export const toLowerCase = (str) => {
  if (str === undefined || str === null) {
    return ''
  }
  return str.toLowerCase()
}
/* ------------------------
    STRING TO UPER CASE
--------------------------*/
export const toUpperCase = (str) => {
  if (str === undefined || str === null) {
    return ''
  }
  return str.toUpperCase()
}
